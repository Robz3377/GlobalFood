// INJECTION IDEMPOTENTE
// ============================================================================
let totalAdded = 0;
let totalSkipped = 0;
const touchedCountries = [];

for (const [countrySlug, newRecipes] of Object.entries(ADDITIONS)) {
  const path = join(COUNTRIES_DIR, `${countrySlug}.json`);
  const country = JSON.parse(readFileSync(path, "utf8"));
  let changed = false;

  for (const recipe of newRecipes) {
    const exists = country.recipes.find((r) => r.slug === recipe.slug);
    if (exists) {
      console.log(`  ⏭  ${countrySlug.padEnd(10)} / ${recipe.slug.padEnd(24)} déjà présent (skip)`);
      totalSkipped += 1;
      continue;
    }
    country.recipes.push(recipe);
    changed = true;
    totalAdded += 1;
    console.log(`  ✓  ${countrySlug.padEnd(10)} / ${recipe.slug.padEnd(24)} ajouté (${recipe.category})`);
  }

  if (changed) {
    writeFileSync(path, JSON.stringify(country, null, 2) + "\n", "utf8");
    touchedCountries.push(countrySlug);
  }
}

console.log(`\n📥 ${totalAdded} recettes ajoutées, ${totalSkipped} déjà présentes`);
console.log(`📁 ${touchedCountries.length} fichiers pays modifiés : ${touchedCountries.join(", ")}`);

// ============================================================================
// RÉGÉNÉRATION data/index.json (lit tous les pays, agnostique aux blocs)
// ============================================================================
const allCountries = [];
for (const file of readdirSync(COUNTRIES_DIR).sort()) {
  if (!file.endsWith(".json")) continue;
  allCountries.push(JSON.parse(readFileSync(join(COUNTRIES_DIR, file), "utf8")));
}

const index = {
  countries: allCountries.map((c) => ({
    slug: c.slug,
    name: c.name,
    iso3: c.iso3,
    isoNumeric: c.isoNumeric,
    flag: c.flag,
    intro: c.intro,
    tagline: c.tagline,
    recipeSlugs: c.recipes.map((r) => r.slug),
  })),
  recipes: allCountries.flatMap((c) =>
    c.recipes.map((r) => ({
      countrySlug: c.slug,
      slug: r.slug,
      title: r.title,
      image: r.image,
      prepTime: r.prepTime,
      cookTime: r.cookTime,
      servings: r.servings,
      diets: r.diets,
      category: r.category,
      seasons: r.seasons ?? [],
      events: r.events ?? [],
      ingredientNames: Array.from(
        new Set(
          [
            ...r.ingredients.map((i) => i.name),
            ...(r.commisIngredients || []).map((i) => i.name),
          ].map((n) => n.trim())
        )
      ),
    }))
  ),
};

writeFileSync(INDEX_PATH, JSON.stringify(index, null, 2) + "\n", "utf8");
console.log(
  `\n✓ data/index.json régénéré : ${index.recipes.length} recettes / ${index.countries.length} pays`
);
